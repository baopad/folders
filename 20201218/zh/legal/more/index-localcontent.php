                        <h3 class="typography-subsection-headline">如何从本服务中移除有关我的信息？</h3>
                        <p>Google 搜索结果显示的是网络上公开提供的内容。搜索引擎无法直接从网站中删除内容，因此删除 Google 的搜索结果并不会将内容从网络中删除。如果您要删除网络上的某些内容，您应该与发布该内容的网站的网站站长联系，让他/她执行相应的操作。如果相关内容已经删除且 Google 注意到了这一更新，该信息就不会再出现在 Google 的搜索结果中了。如果您急需删除某些内容，也可以访问我们的帮助页面以获取详细信息。</p>
                        <h3 class="typography-subsection-headline">网站收集信息会被发送到相关网站</h3>
                        <p>有些情况下会。在您点击了 Google 搜索中的搜索结果后，网络浏览器可能还会将搜索结果页的网址作为引荐来源网址发送给目标网页。搜索结果页的网址有时可能会包含您输入的搜索查询字词。如果您使用的是 SSL 搜索功能（Google 的加密搜索功能），那么在大多数情况下，发送的引荐来源网址中将不会包含您的搜索字词。不过也有一些例外情况，例如，如果您使用的是某些不太常用的浏览器，可能就不是这样。如需关于 SSL 搜索功能的更多信息，请点击此处。可以通过 Google Analytics（分析）或应用编程接口 (API) 查看引荐来源网址中包含的搜索查询字词或信息。此外，广告主可能还会收到致使用户点击广告的确切关键字的相关信息。</p>
                  
                        

